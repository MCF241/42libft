#include "libft.h"

void	ft_putstr_fd(char const *s, int fd)
{
	int		cpt;

	cpt = -1;
	if (s != NULL)
	{
		while (s[++cpt] != '\0')
			write(fd, &s[cpt], 1);
	}
}

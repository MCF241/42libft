#include "libft.h"

void	*ft_bzero(void *s, size_t n)
{
	int i;

	i = 0;
	if (n == 0)
		return (s);
	while (i < (int)n)
	{
		*((char*)s + i) = '\0';
		i++;
	}
	return (s);
}
